"""
Orientação a Objetos com Python
    - POO é um paradigma de programação que utiliza um mapeamento de objetos do mundo real para modelos
    computacionais
    - Paradigmas de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas
    - Principais elementos da OO
        - Classe: Modelo de objeto do mundo real sendo representado computacionalmente;
        - Atributo: Caracterissticas do objeto;
        - Método: Comportamento do objeto (funções);
        - Construtor: Método especial utilizado para criar os objetos;
        - Objeto: Instância da classe.
"""

