print("Forma 1, até numero especificado ")
for num in range(11):
    print(num)

print('-----')
print("Forma 2, inicio e fim")
for val in range(3,11):
    print(val)

print('-----')
print("Forma 3, pulando numeros")
for num1 in range(0, 51, 10):
    print(num1)

print('-----')
print("Forma 4, inversão")
for num2 in range(10,0,-1):
    print(num2)